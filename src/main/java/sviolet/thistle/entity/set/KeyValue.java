package sviolet.thistle.entity.set;

/**
 * 键值对
 * @param <K> key类型
 * @param <V> value类型
 *
 * @author S.Violet
 */
public class KeyValue <K, V> {

    private K key;
    private V value;

    public KeyValue() {
    }

    public KeyValue(K key, V value) {
        this.key = key;
        this.value = value;
    }

    public K getKey() {
        return key;
    }

    public void setKey(K key) {
        this.key = key;
    }

    public V getValue() {
        return value;
    }

    public void setValue(V value) {
        this.value = value;
    }

    public K key(){
        return getKey();
    }

    public V value(){
        return getValue();
    }

}
